class printing {
    public static void main(String[] args) {
        int x = 10, y = 20;

        // yahan pe brackets mein addition ho jata hai
        System.out.println("The sum of: " + x + " and " + y + " is " + (x + y));

        // yahan pe concatenation ho jata hai
        System.out.println("The sum: " + x + y);

        // yahan pe pehle hi addition ho jata hai
        System.out.println(x + y + "The sum ");

        // using comma here
    }
}